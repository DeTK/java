package 문자_리터럴_종류;

public class LiteralEx {
	public static void main(String[] args) {
		System.out.println("줄을바꿔볼까?\n어 바꼈다 ㅋ?"); // 줄바꿈
		System.out.println("탭을 넣어볼까?\t 어 넣어졌네 ㅋ?"); // 탭기능
		System.out.println("\b"); // 백스페이스 기능
		System.out.println("\\"); // \를 문자화
		System.out.println("\'"); // '를 문자화
		System.out.println("\""); // "를 문자화
		System.out.println("\u0042"); // 16진수 0042에 해당하는 문자
	}
}